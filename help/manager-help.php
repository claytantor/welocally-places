
<h1>Places Manager</h1>
<p style="text-align: center;">
<img class="aligncenter" title="place manager" src="http://welocally.com/wp-content/uploads/2011/09/placemgr.png" alt="" /></p>
<p>The Places Manager is a simple way to see what posts are linked to places in one place. A full list of all linked places shows with an edit link to post 
so it can be updated. If you wish to delete a post place just check and item and hit the delete button.
</p>
<ul>
	<li><strong>Using the Place Tag</strong> - Each place has the tag you can use to insert it into your content, just copy and past in the post or page editor window.</li>
	<li><strong>Search Filter</strong> - Will filter paginated results on any keyword in the name ie "Tailor".</li>
	<li><strong>Paginated Controls</strong> - Search results are paginated in 10 place increments, ordered by date added. Use these controls to browse results</li>
	<li><strong>Editing Places</strong> - Editing places will update the information about that place everywhere.</li>
	<li><strong>Deleting Places</strong> - Delete places from the place list.</li>
</ul>

<h2>Editing Places</h2>
<p style="text-align: center;"><img src="http://welocally.com/wp-content/uploads/2011/09/Screen-Shot-2012-05-12-at-9.02.55-AM.png" 
	alt="welocally place manager edit place" title="edit place dialog" width="50%" class="aligncenter size-full wp-image-1253" /></p>
<p>Editing places will update the information about that place everywhere on your site. Use the dialog and save, then close the dialog. 
When you do the information in the place manager should be updated, and everywhere on the site that place is used will get updated as well.
</p>
	
<h2>Deleting Places</h2>
<p style="text-align: center;"><img src="http://welocally.com/wp-content/uploads/2011/09/Screen-Shot-2012-05-12-at-9.33.42-AM.png" alt="" title="delete place dialog"  width="50%" class="aligncenter" /></p>
<p>Deleting places will remove the place from the database as well as the relationship between the place and the post. When you do this the tag is not removed so you will 
have to manually go back to post and remove the tag.</p>
